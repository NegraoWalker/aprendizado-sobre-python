# exemplo1.py
# Atribuindo um número inteiro (int) a uma variável chamada 'idade'
idade = 25

# Atribuindo um número decimal (float) a uma variável chamada 'altura'
altura = 1.75

# Atribuindo um texto (string) a uma variável chamada 'nome'
nome = "Ana Clara"

# Atribuindo um valor booleano (True ou False) a uma variável chamada 'estudante'
estudante = True

# Exibindo todos os valores na tela
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("É estudante?", estudante)

# exemplo2.py
# Variável inteira (int)
quantidade_produtos = 10

# Variável float (número com parte decimal)
preco_unitario = 19.90

# Cálculo do valor total
valor_total = quantidade_produtos * preco_unitario

print("Quantidade de produtos:", quantidade_produtos)
print("Preço unitário:", preco_unitario)
print("Valor total a pagar:", valor_total)

# exemplo3.py
# Variáveis do tipo string (texto)
primeiro_nome = "Carlos"
sobrenome = "Silva"

# Concatenando (juntando) strings
nome_completo = primeiro_nome + " " + sobrenome  # note o espaço em " "

# Exibindo resultado
print("Primeiro nome:", primeiro_nome)
print("Sobrenome:", sobrenome)
print("Nome completo:", nome_completo)

# exemplo4.py
# Outro exemplo: usando f-strings para formatar
idade = 30
mensagem = f"O {nome_completo} tem {idade} anos."
print(mensagem)

